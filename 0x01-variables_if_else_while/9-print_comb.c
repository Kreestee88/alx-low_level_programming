#include <stdio.h>
/**
 * main - Patience, persistence and perspiration make an unbeatable combinatio
 * Return: 0 (Success)
 */

int main(void)
{
	char n;
	int p;

	n = 'a';
	p = 0;
	while
		(p < 10) {
			putchar(p + '0');
			p++;
		}
	while
		(n <= 'f') {
			n++;
		}
	putchar('\n');
	return (0);
}
